    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        try {
            float num1 = Integer.parseInt(jTextFieldPrecio.getText());
            float num2 = Integer.parseInt(jTextFieldUnidades.getText());
            float neto = num1 * num2;
            float des = neto * descuento / 100;
            float iva = neto * this.iva / 100;
            float res = neto + iva - des;
            jLabelResultado.setText(String.valueOf(res));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Tiene que haber un numero ah√≠");
        }
    }//GEN-LAST:event_jButtonCalcularActionPerformed

